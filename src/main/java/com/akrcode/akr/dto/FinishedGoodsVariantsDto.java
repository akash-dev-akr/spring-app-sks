package com.akrcode.akr.dto;

public class FinishedGoodsVariantsDto {
	private String variant;
	private String units_packed;
	private String unit_weight;
	private String total_weight;

	public String getVariant() {
		return variant;
	}

	public void setVariant(String variant) {
		this.variant = variant;
	}

	public String getUnits_packed() {
		return units_packed;
	}

	public void setUnits_packed(String units_packed) {
		this.units_packed = units_packed;
	}

	public String getUnit_weight() {
		return unit_weight;
	}

	public void setUnit_weight(String unit_weight) {
		this.unit_weight = unit_weight;
	}

	public String getTotal_weight() {
		return total_weight;
	}

	public void setTotal_weight(String total_weight) {
		this.total_weight = total_weight;
	}

}
